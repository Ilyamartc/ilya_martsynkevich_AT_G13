package lesson1.pretask;

public class IncrementDecrementExample {
    public static void main(String[] args) {
        int a = 123;
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        --a;
        System.out.println(a);
        ++a;
        System.out.println(a);
    }
}
