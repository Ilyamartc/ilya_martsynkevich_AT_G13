package lesson1.pretask;

public class MathOperatorsExample {
    static int a = 190;
    static int b = 200;
    public static void main(String[] args) {
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println((double) a / (double)b);
    }
}
