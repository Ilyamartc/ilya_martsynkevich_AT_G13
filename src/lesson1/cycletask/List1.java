package lesson1.cycletask;

public class List1 {
    public void numbers() {
        int x =0;
        while (x<=20){
            x++;  //-- используя while вывести в консоль все числа от 0 до 20 в 1 строку через пробел
            System.out.print(x + " ");
        }
        System.out.println();
    }
}
